    private void txtSetningKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSetningKeyTyped
        txtSetning.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                String setning = txtSetning.getText();
                String lowCase = setning.toLowerCase();
                String normalisertString = lowCase.replaceAll("[^a-zæøå_0-9]", "");
                String klarString = normalisertString.toLowerCase();
                lblNormalisert.setText(klarString);
                
                String snudd = snu(klarString);
                lblBaklengs.setText(snudd);
                
                if(snudd.equals(klarString)) {
                    rbtnPalin.setSelected(true);
                    rbtnNotPalin.setSelected(false);
                } else {
                    rbtnNotPalin.setSelected(true);
                    rbtnPalin.setSelected(false);
                }
                
                
                throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
            }
        });
    }//GEN-LAST:event_txtSetningKeyTyped

