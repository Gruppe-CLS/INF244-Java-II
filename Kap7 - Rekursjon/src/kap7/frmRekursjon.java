    public String snu(String streng) {
	if((streng.length() <= 1) || (streng == null)) {
            return streng;
	} else {
            return snu(streng.substring(1)) + streng.charAt(0);
            //String siste = streng.substring(streng.length() - 1));
		
	}
}
    
    
