    public void send(String sendmeg) {
        try {
            Date dato = new Date();
            Socket minServer = new Socket("localhost",2020);
            ObjectOutputStream skriver = new ObjectOutputStream(minServer.getOutputStream());
            skriver.writeUTF(dato.toString() + sendmeg + "\n");
            skriver.flush();
            
            // Leser inn
            ObjectInputStream leser = new ObjectInputStream(minServer.getInputStream());
            sendmeg = leser.readUTF();
            JOptionPane.showMessageDialog(rootPane, sendmeg);
            minServer.close();
            leser.close();
            
            // Null stiller input-felter og fyller listen p√• nytt
            Liste.removeAll();
            Liste.setListData(butlere.values().toArray());
            txtName.setText(null);
            txtNo.setText(null);
            cbCertified.setSelected(false);
            
        } catch (HeadlessException | IOException ex) {
            JOptionPane.showMessageDialog(rootPane, "Feil i klienten: " + ex.getMessage());
        }
    }
  
    
    
