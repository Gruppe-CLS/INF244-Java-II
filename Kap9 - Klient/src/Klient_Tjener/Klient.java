    @SuppressWarnings({"unchecked", "unchecked"})
    private void btnAddUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddUpdateActionPerformed
        int no = Integer.parseInt(txtNo.getText());
        String name = txtName.getText();
        boolean certified = false;
        if(cbCertified.isSelected()) {
            certified = true;
        }
        Butler nyButler = new Butler(no, name, certified);
        butlere.put(no, nyButler);
        
        // Sender ny informasjon til Tjener/Server
        String sendDenne = ", ADD/UPDATE: " + nyButler.toString();
        send(sendDenne);
        
        
        
        
    }//GEN-LAST:event_btnAddUpdateActionPerformed

    @SuppressWarnings("unchecked")
    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        Butler slettMeg = (Butler) Liste.getSelectedValue();
        int slettNo = slettMeg.getNo();
        butlere.remove(slettNo);
        Liste.removeAll();
        Liste.setListData(butlere.values().toArray());
        JOptionPane.showMessageDialog(rootPane, "Butler: " + slettMeg + ", has been deleted.");
        
        // Sender ny informasjon til Tjener/Server
        String sendDenne = ", DELETE: " + slettMeg.toString();
        send(sendDenne);
        
    }//GEN-LAST:event_btnDeleteActionPerformed

